export function getSkillLibrary(): SkillExercise[] {
  return EXTENDED_SKILL_LIBRARY;
}

export function getSkillsByCategory(categoryId: string): SkillExercise[] {
  return EXTENDED_SKILL_LIBRARY.filter(s => s.category === categoryId);
}

export function getSkillById(id: string): SkillExercise | null {
  return EXTENDED_SKILL_LIBRARY.find(s => s.id === id) ?? null;
}

export function getCategoryInfo(categoryId: string): SkillCategory | null {
  return SKILL_CATEGORIES.find(c => c.id === categoryId) ?? null;
}
